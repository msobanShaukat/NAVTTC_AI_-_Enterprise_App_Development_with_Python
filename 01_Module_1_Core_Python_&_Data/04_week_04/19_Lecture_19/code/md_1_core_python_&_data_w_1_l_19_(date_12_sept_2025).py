def add_post():
    '''Collect user input and insert a new blog post'''
    try:
        with sqlite3.connect("blog.db") as conn:
            # Enable foreign key support
            conn.execute("PRAGMA foreign_keys = ON")
            cur = conn.cursor()
            
            # Collect user input
            print("\nğŸ“ ADD NEW BLOG POST")
            print("-" * 30)
            
            title = input("Enter post title: ").strip()
            content = input("Enter post content: ").strip()
            author_id = input("Enter author ID: ").strip()
            
            # Validate input
            if not title:
                print("âŒ Error: Title cannot be empty")
                return
                
            if not content:
                print("âŒ Error: Content cannot be empty")
                return
                
            if not author_id.isdigit():
                print("âŒ Error: Author ID must be a number")
                return
                
            author_id = int(author_id)
            
            # Verify author exists
            cur.execute("SELECT 1 FROM Users WHERE UserID = ?", (author_id,))
            if cur.fetchone() is None:
                print(f"âŒ Error: Author with ID {author_id} does not exist")
                return
            
            # Insert the post
            cur.execute(
                "INSERT INTO Posts (Title, Content, AuthorID) VALUES (?, ?, ?)",
                (title, content, author_id)
            )
            
            # Get the ID of the newly inserted post
            post_id = cur.lastrowid
            
            conn.commit()
            print(f"âœ… Post inserted successfully! Post ID: {post_id}")
            
    except sqlite3.Error as e:
        print(f"âŒ Database error: {e}")
    except Exception as e:
        print(f"âŒ Unexpected error: {e}")

